package com.yowyob.tiibntick.core.marketback.application.port.out;

import com.yowyob.tiibntick.core.marketback.domain.model.*;
import reactor.core.publisher.Flux;
import reactor.core.publisher.Mono;
import java.math.BigDecimal;
import java.util.UUID;

/**
 * Outbound port — MarketOrder persistence contract.
 *
 * @author MANFOUO Braun
 */
public interface IMarketOrderRepository {

    Mono<MarketOrder> save(MarketOrder order);

    Mono<MarketOrder> findById(MarketOrderId id, String tenantId);

    Flux<MarketOrder> findByClientId(UUID clientId, String tenantId);

    Flux<MarketOrder> findByProviderId(UUID providerId, String tenantId);

    Flux<MarketOrder> findByStatus(OrderStatus status, String tenantId);

    Flux<MarketOrder> findByListingId(MarketListingId listingId);

    /** Total order count for the tenant (stats overview). */
    Mono<Long> countByTenantId(String tenantId);

    /** Count of COMPLETED orders for the tenant (stats overview). */
    Mono<Long> countCompletedByTenantId(String tenantId);

    /** Count of orders for a specific provider in a tenant. */
    Mono<Long> countByProviderIdAndTenantId(UUID providerId, String tenantId);

    /** Sum of total revenue generated by a specific provider. */
    Mono<BigDecimal> sumRevenueByProviderId(UUID providerId, String tenantId);

    Mono<Void> delete(MarketOrderId id);
}
